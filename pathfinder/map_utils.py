def add_slow_rectangles(map_array, bottom_rect=None, top_rect=None, mode=1):
    map_new = np.copy(map_array)
    # Bottom rectangle
    if mode in (2, 3) and bottom_rect is not None:
        x_left, y_top, width, height = bottom_rect
        for y in range(y_top, y_top + height):
            for x in range(x_left, x_left + width):
                if 0 <= x < map_new.shape[1] and 0 <= y < map_new.shape[0]:
                    if map_new[y, x] == 0:
                        map_new[y, x] = 3
    # Top rectangle
    if mode in (3, 4) and top_rect is not None:
        x_left, y_top, width, height = top_rect
        for y in range(y_top, y_top + height):
            for x in range(x_left, x_left + width):
                if 0 <= x < map_new.shape[1] and 0 <= y < map_new.shape[0]:
                    if map_new[y, x] == 0:
                        map_new[y, x] = 3
    return map_new